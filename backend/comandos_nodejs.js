//! Instalação do Node.js

//? -> A instalação do Node.js é bem simples, basta acessar o site oficial do Node.js e baixar o instalador para o seu sistema operacional.

//? -> npm = Node Package Manager -> Gerenciador de pacotes do Node.js que permite instalar bibliotecas de terceiros

//? -> node = runtime do javascript

//? -> Para rodar os imports em modulos, deve-se ir no arquivo package.json e adicionar "type": "module" no objeto principal


//! Comandos Node.js

//* npm init -y -> cria um arquivo package.json com as configurações padrão

//* node server.js -> executa o arquivo server.js

//* npm run dev -> executa o script definido no package.json

//* npm install fastify -> instala a biblioteca fastify que serve para criar servidores web em Node.js

//* npm i dotenv -D -> instala a biblioteca dotenv como dependência de desenvolvimento

//* npm install pg -> instala a biblioteca pg que serve para fazer conexão com o banco de dados PostgreSQL

//* npm install postgres -> instala a biblioteca postgres que serve para fazer conexão com o banco de dados PostgreSQL